export function getFlag(name: string): string {
  if (!name || typeof name !== 'string') return "рџЊЌ";
  
  const cleanName = name.trim();
  if (nationalityFlags[cleanName]) return nationalityFlags[cleanName];
  
  const lowerName = cleanName.toLowerCase();
  
  // Try exact lowecase match
  const match = Object.entries(nationalityFlags).find(([key]) => key.toLowerCase() === lowerName);
  if (match) return match[1];

  // Try partial match (e.g. "Arabs" -> "Saudi Arabia" or similar if needed)
  if (lowerName.includes('england')) return "рџЏґу Ѓ§у Ѓўу ЃҐу Ѓ®у Ѓ§у Ѓї";
  if (lowerName.includes('spain')) return "рџ‡Єрџ‡ё";
  if (lowerName.includes('brazil')) return "рџ‡§рџ‡·";
  if (lowerName.includes('argentina')) return "рџ‡¦рџ‡·";
  if (lowerName.includes('portugal')) return "рџ‡µрџ‡№";
  if (lowerName.includes('italy')) return "рџ‡®рџ‡№";
  if (lowerName.includes('france')) return "рџ‡«рџ‡·";
  if (lowerName.includes('germany')) return "рџ‡©рџ‡Є";
  
  return "рџЊЌ";
}

/**
 * Converts a display country name to the shortest stem for ilike DB matching.
 * 'Argentina' => 'argentin'  (matches 'Argentine', 'Argentinian')
 * 'Brazil'    => 'brazil'    (matches 'Brazilian')
 * The stem is lowercased; use it as: .ilike('nationality', `%${stem}%`)
 */
export function toNatStem(name: string): string {
    if (!name) return '';
    let s = name.toLowerCase().trim();
    // Remove common country-name suffixes to reach the adjectival root
    const suffixes = ['inia', 'olia', 'onia', 'alia', 'eria', 'uria', 'ania', 'ina', 'ia', 'na', 'land', 'stan'];
    for (const suf of suffixes) {
        if (s.endsWith(suf) && s.length - suf.length >= 4) {
            return s.slice(0, s.length - suf.length);
        }
    }
    return s;
}
